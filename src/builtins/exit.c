#include "shell.h"

/*------------------------------*/
/* minishell exit				*/
/* built-in main function		*/
/*------------------------------*/
int	sh_exit(struct s_shell *sh, char **argv)
{
	(void)argv;
	sh_cleanup(sh);
	write(2, "exit\n", 5);
	exit(0);
	return (0);
}
